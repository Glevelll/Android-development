package com.project.foodapp.ui.Recipes;

import android.graphics.Bitmap;

public class Card {
    private int id;
    private String name;

    public Card(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
